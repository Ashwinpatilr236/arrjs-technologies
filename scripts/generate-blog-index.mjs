import fs from 'fs';
import path from 'path';
import matter from 'gray-matter';
import {remark} from 'remark';
import html from 'remark-html';

const blogDir = path.resolve(process.cwd(), 'content', 'blog');
const outFile = path.resolve(process.cwd(), 'src', 'data', 'blogPosts.generated.ts');

if (!fs.existsSync(blogDir)) {
  console.error('No blog directory found at', blogDir);
  process.exit(0);
}

const files = fs.readdirSync(blogDir).filter(f => f.endsWith('.md'));

async function render() {
  const posts = [];
  for (const file of files) {
    const full = path.join(blogDir, file);
    const raw = fs.readFileSync(full, 'utf8');
    const {data, content} = matter(raw);
    const processed = await remark().use(html).process(content);
    const htmlContent = String(processed);
    posts.push({
      id: data.id || file.replace(/\.md$/, ''),
      title: data.title || 'Untitled',
      category: data.category || 'Blog',
      excerpt: data.excerpt || '',
      date: data.date || new Date().toISOString(),
      readTime: data.readTime || '',
      slug: data.slug || file.replace(/\.md$/, ''),
      html: htmlContent,
    });
  }

  const ts = `// Auto-generated by scripts/generate-blog-index.mjs
export const blogPosts = ${JSON.stringify(posts, null, 2)};
`;
  fs.writeFileSync(outFile, ts, 'utf8');
  console.log('Wrote', outFile);
}

render().catch(err => {
  console.error(err);
  process.exit(1);
});
